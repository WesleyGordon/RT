#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i;
	int end;

	n++;
	end = ft_strlen(s1) - 1;
	i = -1;
	while (s2[++i] != '\0' && --n > 0)
		s1[++end] = s2[i];
	s1[++end] = '\0';
	return (s1);
}
