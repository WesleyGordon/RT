#include "libft.h"

void		*ft_memset(void *b, int c, size_t len)
{
	int		i;
	char	*b2;

	b2 = b;
	i = 0;
	len++;
	while (--len > 0)
		b2[i++] = (unsigned char)c;
	b = b2;
	return (b);
}
