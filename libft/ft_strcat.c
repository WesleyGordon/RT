#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int end;

	end = ft_strlen(s1) - 1;
	i = -1;
	while (s2[++i] != '\0')
		s1[++end] = s2[i];
	s1[++end] = '\0';
	return (s1);
}
